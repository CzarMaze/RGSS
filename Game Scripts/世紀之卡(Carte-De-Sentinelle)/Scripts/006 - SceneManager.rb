#encoding:utf-8
#==============================================================================
# ■ SceneManager
#------------------------------------------------------------------------------
# 　場景切換。RGSS3 內置的新功能，在使用 call 方法切換到新場景時，可以
#   用 return 返回上一個場景。
#==============================================================================

module SceneManager
  #--------------------------------------------------------------------------
  # ● 模塊的實例變量
  #--------------------------------------------------------------------------
  @scene = nil                            # 當前場景實例
  @stack = []                             # 場景切換的記錄
  @background_bitmap = nil                # 背景用的場景截圖
  #--------------------------------------------------------------------------
  # ● 運行
  #--------------------------------------------------------------------------
  def self.run
    DataManager.init
    Audio.setup_midi if use_midi?
    @scene = first_scene_class.new
    @scene.main while @scene
  end
  #--------------------------------------------------------------------------
  # ● 獲取最初場景的所屬類
  #--------------------------------------------------------------------------
  def self.first_scene_class
    $BTEST ? Scene_Battle : Scene_Title
  end
  #--------------------------------------------------------------------------
  # ● 是否使用 MIDI 
  #--------------------------------------------------------------------------
  def self.use_midi?
    $data_system.opt_use_midi
  end
  #--------------------------------------------------------------------------
  # ● 獲取當前場景
  #--------------------------------------------------------------------------
  def self.scene
    @scene
  end
  #--------------------------------------------------------------------------
  # ● 判定當前場景的所屬類
  #--------------------------------------------------------------------------
  def self.scene_is?(scene_class)
    @scene.instance_of?(scene_class)
  end
  #--------------------------------------------------------------------------
  # ● 直接切換某個場景（無過渡）
  #--------------------------------------------------------------------------
  def self.goto(scene_class)
    @scene = scene_class.new
  end
  #--------------------------------------------------------------------------
  # ● 切換
  #--------------------------------------------------------------------------
  def self.call(scene_class)
    @stack.push(@scene)
    @scene = scene_class.new
  end
  #--------------------------------------------------------------------------
  # ● 返回到上一個場景
  #--------------------------------------------------------------------------
  def self.return
    @scene = @stack.pop
  end
  #--------------------------------------------------------------------------
  # ● 清空場景切換的記錄
  #--------------------------------------------------------------------------
  def self.clear
    @stack.clear
  end
  #--------------------------------------------------------------------------
  # ● 退出游戲
  #--------------------------------------------------------------------------
  def self.exit
    @scene = nil
  end
  #--------------------------------------------------------------------------
  # ● 生成背景用的場景截圖
  #--------------------------------------------------------------------------
  def self.snapshot_for_background
    @background_bitmap.dispose if @background_bitmap
    @background_bitmap = Graphics.snap_to_bitmap
    @background_bitmap.blur
  end
  #--------------------------------------------------------------------------
  # ● 獲取背景用的場景截圖
  #--------------------------------------------------------------------------
  def self.background_bitmap
    @background_bitmap
  end
end
