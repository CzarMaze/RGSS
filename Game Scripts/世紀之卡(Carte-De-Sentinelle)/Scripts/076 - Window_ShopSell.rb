#==============================================================================
# ■ Window_ShopSell
#------------------------------------------------------------------------------
# 　商店畫面中，賣出時顯示持有物品的窗口。
#==============================================================================

class Window_ShopSell < Window_ItemList
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(x, y, width, height)
    super(x, y, width, height)
  end
  #--------------------------------------------------------------------------
  # ● 獲取選擇項目的有效狀態
  #--------------------------------------------------------------------------
  def current_item_enabled?
    enable?(@data[index])
  end
  #--------------------------------------------------------------------------
  # ● 查詢物品是否可賣
  #--------------------------------------------------------------------------
  def enable?(item)
    item && item.price > 0
  end
end
