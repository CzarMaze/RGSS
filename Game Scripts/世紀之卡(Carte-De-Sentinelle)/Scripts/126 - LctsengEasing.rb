module Lctseng
class Easing
  #--------------------------------------------------------------------------
  # ● 初始化
  #--------------------------------------------------------------------------
  def initialize(v0 = 0,duration = 0,distance = 0)
    setup(v0,duration,distance)
  end
  #--------------------------------------------------------------------------
  # ● 設置資料
  #--------------------------------------------------------------------------
  def setup(v0,duration,distance)
    @v0 = v0
    @duration = duration
    @distance = distance
  end
  #--------------------------------------------------------------------------
  # ● Exponential-Out函數
  #--------------------------------------------------------------------------
  def evalExpoOut(time)
    return @distance * ( -( 2 ** (-10 * time.to_f/@duration) ) + 1 ) + @v0;
  end
  #--------------------------------------------------------------------------
  # ● Circular-Out函數
  #--------------------------------------------------------------------------
  def evalCircOut(time)
    time /= @duration.to_f
  	time-=1
    return @distance * Math.sqrt(1 - time*time) + @v0;
  end
end
end