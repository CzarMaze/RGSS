#------------------------------------------------------------------------------
# 　包裝角色數組的外殼。本類的實例請參考 $game_actors 。
#==============================================================================

class Game_Actors
  #--------------------------------------------------------------------------
  # ● 定義實例變數
  #--------------------------------------------------------------------------
  attr_reader :buy_card_hash
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize
    @buy_card_hash = {}
    @data = []
  end
  #--------------------------------------------------------------------------
  # ● 獲取角色
  #--------------------------------------------------------------------------
  def [](actor_id)
    return nil if actor_id > 6
    @data[actor_id] ||= CardBattle::Game_BattleActor.new(actor_id)
  end
  #--------------------------------------------------------------------------
  # ● 獲得卡片
  #--------------------------------------------------------------------------
  def gain_card(cid,num)
    CardBattle.story_actor_ids.each do |id|
      puts "角色#{id}獲得購買卡片！"
      actor = $game_actors[id]
      actor.gain_card(cid,num,:store)
    end
  end
end
