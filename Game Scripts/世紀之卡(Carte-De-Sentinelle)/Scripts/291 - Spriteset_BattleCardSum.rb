#==============================================================================
# ■ CardBattle::Spriteset_BattleCardSum
#------------------------------------------------------------------------------
# 顯示總和數值的精靈
#==============================================================================
module CardBattle
class Spriteset_BattleCardSum
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(card_set,viewport)
    @card_set = card_set
    @viewport = viewport
    create_sprites
  end
  #--------------------------------------------------------------------------
  # ● 產生精靈
  #--------------------------------------------------------------------------
  def create_sprites
    @number = Sprite_BattlePointSumNumber.new(@viewport)
    @number.set_pos(@card_set.sum_sprite_pos)
  end
  #--------------------------------------------------------------------------
  # ● 釋放
  #--------------------------------------------------------------------------
  def dispose
    @number.dispose
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def update
    @number.update
  end
  #--------------------------------------------------------------------------
  # ● 是否效果中？
  #--------------------------------------------------------------------------
  def effect?
   @number.effect?
 end
  #--------------------------------------------------------------------------
  # ● 顯示卡片總和
  #--------------------------------------------------------------------------
 def show_card_sum
   mode = @card_set.battle_mode
   skill = @card_set.skill_data
   point = @card_set.point_sum
   @number.draw_number(mode,skill,point)
   @number.show
 end
  #--------------------------------------------------------------------------
  # ● 隱藏卡片總和
  #--------------------------------------------------------------------------
 def hide_card_sum
   @number.hide
 end

end
end