module CardBattle
class Sprite_WinBonus < Sprite_Base
  #--------------------------------------------------------------------------
  # ● 加入模組
  #--------------------------------------------------------------------------
  include SpriteFader
  include SpriteZoomer
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(viewport = nil)
    super(viewport)
    create_bitmap
    self.opacity = 0
    center_origin
    zoomer_init
    fader_init
    @show = false
  end
  #--------------------------------------------------------------------------
  # ● 產生圖案
  #--------------------------------------------------------------------------
  def create_bitmap
    self.bitmap = Cache.win('Bonus_Word')
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def update
    super
    fader_update
    zoomer_update
  end
  #--------------------------------------------------------------------------
  # ● 顯示
  #--------------------------------------------------------------------------
  def show
    return if @show
    @show = true
    fader_set_fade(255,10)
    start_animation_id(131)
  end
  #--------------------------------------------------------------------------
  # ● 隱藏
  #--------------------------------------------------------------------------
  def hide
    return if !@show
    @show = false
    fader_set_fade(0,15)
    zoomer_set_effect([1.0,1.0],[1.5,1.5],15)
    start_animation_id(132)
  end
end
end
