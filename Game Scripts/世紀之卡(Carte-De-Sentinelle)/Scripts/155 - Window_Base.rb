#==============================================================================
# ■ Window_Base
#------------------------------------------------------------------------------
# 　游戲中所有窗口的父類
#==============================================================================

class Window_Base < Window
  #--------------------------------------------------------------------------
  # ● 是否在視窗範圍內？此為視窗自身延伸定義
  #       關閉預設值，避免其他爭議BUG
  #--------------------------------------------------------------------------
  def in_window?
    return false
  end
  #--------------------------------------------------------------------------
  # ● 預設值：是否在視窗內？
  #--------------------------------------------------------------------------
  def default_in_window?
    Mouse.area?(self.x, self.y, self.width, self.height)
  end
  #--------------------------------------------------------------------------
  # ● 是否打開視窗中？
  #--------------------------------------------------------------------------
  def opening?
    @opening
  end
  #--------------------------------------------------------------------------
  # ● 是否關閉視窗中？
  #--------------------------------------------------------------------------
  def closing?
    @closing
  end
  #--------------------------------------------------------------------------
  # ● 是否還看的見
  #--------------------------------------------------------------------------
  def visible?
    self.visible && self.openness > 0
  end

end
