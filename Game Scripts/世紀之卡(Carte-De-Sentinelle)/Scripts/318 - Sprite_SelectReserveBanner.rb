module CardBattle
class Sprite_SelectReserveBanner < Sprite_Base
  #--------------------------------------------------------------------------
  # ● 加入淡入淡出模組
  #--------------------------------------------------------------------------
  include SpriteFader
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(viewport)
    super(viewport)
    create_bitmap
    self.x = 0
    self.y = 100
    self.z = 100
    self.opacity = 0
    fader_init
  end
  #--------------------------------------------------------------------------
  # ● 產生點陣圖
  #--------------------------------------------------------------------------
  def create_bitmap
    self.bitmap = Cache.battle("battle_card_select")
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def update
    super
    fader_update
  end
end
end
