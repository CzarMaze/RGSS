module Lctseng
class Sprite_ActorStory < Sprite_Base
  #--------------------------------------------------------------------------
  # ● 加入設定模組
  #--------------------------------------------------------------------------
  include SpriteSlider
  include SpriteFader
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(viewport = nil)
    super(viewport)
    self.opacity = 0
    slider_init
    fader_init
  end
  #--------------------------------------------------------------------------
  # ● 釋放
  #--------------------------------------------------------------------------
  def dispose
    super
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def update
    super
    slider_update
    fader_update
  end
  #--------------------------------------------------------------------------
  # ● 開始位置
  #--------------------------------------------------------------------------
  def start_pos
    [-100,79]
  end
  #--------------------------------------------------------------------------
  # ● 結束位置
  #--------------------------------------------------------------------------
  def end_pos
    [0,79]
  end
  #--------------------------------------------------------------------------
  # ● 顯示
  #--------------------------------------------------------------------------
  def show(index)
    self.opacity = 0
    self.bitmap = Cache.picture("storyline_actor_#{index+1}")
    slider_set_move(start_pos,end_pos,20)
    fader_set_fade(255,20)
  end
end
end
