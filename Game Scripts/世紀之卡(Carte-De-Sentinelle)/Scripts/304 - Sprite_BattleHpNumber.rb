#==============================================================================
# ■ CardBattle::Sprite_BattleHpNumber
#------------------------------------------------------------------------------
# 　顯示生命值數值的精靈
#==============================================================================
module CardBattle
class Sprite_BattleHpNumber < Sprite_FadingNumber
  #--------------------------------------------------------------------------
  # ● 建立圖片
  #--------------------------------------------------------------------------
  def create_bitmap
    self.bitmap = Bitmap.new(125,24)
    self.bitmap.font = BATTLE_HP_FONT
  end
  #--------------------------------------------------------------------------
  # ● 上升時處理
  #--------------------------------------------------------------------------
  def process_on_increase
    start_animation_id(115) # 恢復的動畫
  end
  #--------------------------------------------------------------------------
  # ● 下降時處理
  #--------------------------------------------------------------------------
  def process_on_decrease
    start_animation_id(7) # 被打的動畫
  end
  #--------------------------------------------------------------------------
  # ● 歸零時處理
  #--------------------------------------------------------------------------
  def process_on_zero
    start_animation_id(114)
  end
  #--------------------------------------------------------------------------
  # ● 重繪數值
  #--------------------------------------------------------------------------
  def redraw_value(val)
    self.bitmap.clear
    draw_text(self.bitmap.rect,"HP：#{val}",2)
  end
end
end
