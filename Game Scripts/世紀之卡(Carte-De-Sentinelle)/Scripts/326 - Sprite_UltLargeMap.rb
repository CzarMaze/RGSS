class Sprite_UltLargeMap < Sprite
  #--------------------------------------------------------------------------
  # ● 加入設定模組
  #--------------------------------------------------------------------------
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(viewport = nil)
    super(viewport)
    @count = 0
    @opacity_speed = 0
    self.opacity = 0
    self.visible = false
  end
  #--------------------------------------------------------------------------
  # ● 淡入
  #--------------------------------------------------------------------------
  def fade_in
    self.bitmap.dispose if self.bitmap
    self.bitmap  = Cache.picture("orange")
    @count = 20
    @opacity_speed = (100.0 / @count).floor
    self.visible = true
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def update
    super
    if @count > 0
      @count -= 1
      self.opacity += @opacity_speed
    end
  end
  #--------------------------------------------------------------------------
  # ● 淡出
  #--------------------------------------------------------------------------
  def fade_out
    @count = 20
    @opacity_speed = @opacity_speed * -1
  end
  
end
