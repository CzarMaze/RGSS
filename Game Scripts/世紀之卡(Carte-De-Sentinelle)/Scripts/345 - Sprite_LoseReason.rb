#==============================================================================
# ■ CardBattle::Sprite_LoseReason
#------------------------------------------------------------------------------
# 　顯示失敗原因的精靈
#==============================================================================
module CardBattle
class Sprite_LoseReason < Sprite_Base
  #--------------------------------------------------------------------------
  # ● 加入模組
  #--------------------------------------------------------------------------
  include SpriteFader
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(viewport = nil)
    super(viewport)
    create_bitmap
    self.opacity = 0
    self.flash(Color.new(255,255,255),120)
    fader_init
    center_sprite
    fader_set_fade(255,30)
  end
  #--------------------------------------------------------------------------
  # ● 產生圖片
  #--------------------------------------------------------------------------
  def create_bitmap
    name = $game_system.battle_result.lose_type.id2name
    self.bitmap = Cache.lose(name)
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def update
    super
    fader_update
  end
end
end
