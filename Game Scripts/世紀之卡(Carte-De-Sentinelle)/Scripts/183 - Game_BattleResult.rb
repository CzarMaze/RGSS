module CardBattle
class Game_BattleResult
  #--------------------------------------------------------------------------
  # ● 定義實例變量
  #--------------------------------------------------------------------------
  attr_accessor :win_flag # 是否勝利
  attr_accessor :lose_type # 失敗類別(:hp與:card)
  attr_accessor :actor_id # 角色ID
  attr_accessor :pre_info # 升級前資訊
  attr_accessor :post_info # 升級後資訊
  attr_accessor :gold
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize
    clear_all_data
  end
  #--------------------------------------------------------------------------
  # ● 清除所有資料
  #--------------------------------------------------------------------------
  def clear_all_data
    @win_flag = false
    @lose_type = nil
    @gold = 0
  end
  #--------------------------------------------------------------------------
  # ● 設置勝利
  #--------------------------------------------------------------------------
  def set_win(actor_id)
    @actor_id = actor_id
    @win_flag = true
    @lose_type = nil
  end
  #--------------------------------------------------------------------------
  # ● 設置失敗
  #--------------------------------------------------------------------------
  def set_lose(reason)
    @win_flag = false
    @lose_type = reason
  end
  #--------------------------------------------------------------------------
  # ● 設置升級前資訊
  #--------------------------------------------------------------------------
  def set_pre_info(p_cards)
    @pre_info = p_cards.battler.data_dump
  end
  #--------------------------------------------------------------------------
  # ● 設置升級後資訊
  #--------------------------------------------------------------------------
  def set_post_info(p_cards)
    @post_info = p_cards.battler.data_dump
  end
  
end
end