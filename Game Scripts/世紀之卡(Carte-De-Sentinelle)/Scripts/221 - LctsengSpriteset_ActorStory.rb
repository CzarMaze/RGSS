#     處理選角色故事精靈組
#==============================================================================
module Lctseng
class Spriteset_ActorStory
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(index,viewport)
    @viewport = viewport
    @index = index
    create_spritesets
  end
  #--------------------------------------------------------------------------
  # ● 產生精靈組
  #--------------------------------------------------------------------------
  def create_spritesets
    # 文字
    @base = Sprite_ActorStoryText.new(@viewport_base)
    # 立繪
    @picture = Sprite_ActorStory.new(@viewport_base)
    
  end
  #--------------------------------------------------------------------------
  # ● 釋放
  #--------------------------------------------------------------------------
  def dispose
    dispose_spritesets
  end
  #--------------------------------------------------------------------------
  # ● 釋放精靈組
  #--------------------------------------------------------------------------
  def dispose_spritesets
    @picture.dispose
    @base.dispose
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def update
    update_spritesets
  end
  #--------------------------------------------------------------------------
  # ● 更新精靈組
  #--------------------------------------------------------------------------
  def update_spritesets
    @base.update
    @picture.update
  end
  #--------------------------------------------------------------------------
  # ● 是否需等待效果
  #--------------------------------------------------------------------------
  def wait_effect?
    false
  end
  #--------------------------------------------------------------------------
  # ● 刷新
  #--------------------------------------------------------------------------
  def refresh(index)
    @index = index
    show
  end
  #--------------------------------------------------------------------------
  # ● 顯示
  #--------------------------------------------------------------------------
  def show
    @base.show(@index)
    @picture.show(@index)
  end
end
end