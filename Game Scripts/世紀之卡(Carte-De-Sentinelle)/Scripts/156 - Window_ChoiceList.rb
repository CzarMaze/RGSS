#==============================================================================
# ■ Window_ChoiceList
#------------------------------------------------------------------------------
# 　此窗口使用于事件指令中的“顯示選項”的功能。
#==============================================================================

class Window_ChoiceList < Window_Command
  #--------------------------------------------------------------------------
  # ● “確定”和“取消”的處理
  #--------------------------------------------------------------------------
  def process_handling
    return if $game_system.disable_message_continue
    return unless in_window?  #  除果滑鼠在視窗外就不反應
    super
  end
  #--------------------------------------------------------------------------
  # ● 是否在視窗內？
  #--------------------------------------------------------------------------
  def in_window?
    default_in_window? 
  end
end
