#==============================================================================
# ■ Scene_Save
#------------------------------------------------------------------------------
# 　存檔畫面
#==============================================================================

class Scene_Save < Scene_File
  #--------------------------------------------------------------------------
  # ● 取得精靈組class
  #--------------------------------------------------------------------------
  def spriteset_class
    Lctseng::Spriteset_Save
  end
  #--------------------------------------------------------------------------
  # ● 確定存檔文件
  #--------------------------------------------------------------------------
  def on_savefile_ok
    super
    puts "確認存檔：#{@index}"
    if DataManager.save_game(@index)
      on_save_success
    else
      Sound.play_buzzer
    end
  end
  #--------------------------------------------------------------------------
  # ● 存檔成功時的處理
  #--------------------------------------------------------------------------
  def on_save_success
    Sound.play_save
    return_scene
  end
end
