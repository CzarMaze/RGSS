module Lctseng
class Sprite_SelectActorSave < Sprite_SingleButton
  #--------------------------------------------------------------------------
  # ● 加入設定模組
  #--------------------------------------------------------------------------
  include SpriteSlider
  include SpriteFader
  #--------------------------------------------------------------------------
  # ● 初始化對象
  #--------------------------------------------------------------------------
  def initialize(viewport)
    super([480,425],'storyline_save',viewport)
    self.opacity = 0
    fader_init
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def update
    super
    fader_update
  end
  #--------------------------------------------------------------------------
  # ● 顯示
  #--------------------------------------------------------------------------
  def show
    super
    self.opacity = 0
    fader_set_fade(255,40)
  end
end
end
