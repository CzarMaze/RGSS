#==============================================================================
# ■ Cache
#------------------------------------------------------------------------------
#  處理載入圖像，建立/儲保 Bitmap 物件。為加快載入速度和節省內存，
#  此模塊將建立的 bitmap 物件保存在內部哈希表中，令程序要再次處理
# 　相同的圖像時能快速讀取。
#==============================================================================

module Cache
  #--------------------------------------------------------------------------
  # ● 獲取“戰鬥”圖像
  #--------------------------------------------------------------------------
  def self.battle(filename)
    load_bitmap("Graphics/Battle/", filename)
  end
  #--------------------------------------------------------------------------
  # ● 獲取“戰鬥失敗”圖像
  #--------------------------------------------------------------------------
  def self.lose(filename)
    load_bitmap("Graphics/Battle/Lose/", filename)
  end
  #--------------------------------------------------------------------------
  # ● 獲取“戰鬥勝利”圖像
  #--------------------------------------------------------------------------
  def self.win(filename)
    load_bitmap("Graphics/Battle/Win/", filename)
  end

  #--------------------------------------------------------------------------
  # ● 獲取“標題”相關圖像
  #--------------------------------------------------------------------------
  def self.title(filename)
    load_bitmap("Graphics/Title/", filename)
  end
  #--------------------------------------------------------------------------
  # ● 獲取“整備”圖像
  #--------------------------------------------------------------------------
  def self.prepare(filename)
    load_bitmap("Graphics/Battle/Prepare/", filename)
  end
  #--------------------------------------------------------------------------
  # ● 獲取“購買”圖像
  #--------------------------------------------------------------------------
  def self.buy(filename)
    load_bitmap("Graphics/Battle/Buy/", filename)
  end
  
end

